package Class23;

public class Class23_4_NestedForLoop {
    public static void main(String[] args) {

//        print out multiplication table

        for(int i=1; i<=10;i++){

            for(int x = 1; x<=10; x++){
                System.out.print(i*x +"\t\t");
            }
System.out.println();
        }
    }
}
